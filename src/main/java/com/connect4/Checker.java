package com.connect4;

/**
 * Represents a checker piece in Connect 4. Checkers for the two players are either red or yellow.
 */
public enum Checker {
    /**
     * A red piece
     */
    RED,

    /**
     * A yellow piece
     */
    YELLOW,

    /**
     * A blank piece which represents no piece
     */
    CLEAR
}
